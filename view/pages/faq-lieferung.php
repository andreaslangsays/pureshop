	<h2 id="faq-wann">Wann kann ich mit der Lieferung rechnen?</h2>
	<p>Die Ware wird am Tag Ihrer Bestellung, spätestens jedoch am nächsten Werktag versendet. Die Laufzeit unseres Paketdienstes beträgt derzeit 1-2 Werktage.</p>
	<br /><br />
	
