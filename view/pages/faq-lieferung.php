	<h2 id="faq-bestellung-aendern">Kann ich eine Bestellung ändern?</h2>
	<p>Falls die Bestellung noch nicht in der Konfektionierung ist, ja. Allerdings ist das Zeitfenster zwischen Bestelleingang bei uns und Versandvorbereitung sehr klein.</p>
	<br /><br />
	
