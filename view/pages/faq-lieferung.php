	<h2 id="faq-wunschtermin">Kann ich einen Wunschtermin der Lieferung angeben?</h2>
	<p>Nein. Wir haben keinen Einfluss auf die Paketdienste. Es sei denn, wir versenden via Express mit garantierter Zustellung. Das machen wir gern, allerdings kommen bei dieser Versandart Zusatzkosten auf Sie zu.</p>
