	<h2 id="faq-lieferung-beschaedigt">Die Lieferung war falsch bzw. beschädigt. Was nun?</h2>
	<p>Sollte die Sendung beim Eintreffen sichtbar beschädigt sein, verweigern Sie bitte mit Verweis darauf (!) die Annahme.
Anschließend kontaktieren Sie bitte unsere Reklamationsabteilung unter <span class="tx_blue tx_12_15">+49.(0)30 284 700 0</span>. Dies gilt auch für den Fall, dass Sie die Beschädigung der Sendung erst nach dem Öffnen bemerkt haben.</p>
	<br /><br />
	
	<h2 id="faq-zusteller">Welcher Zusteller liefert aus?</h2>
	<p>Wir liefern mit UPS aus. Größere Sendungen erhalten Sie auf Palette via Spedition.</p>
	<br /><br />
	
	<h2 id="faq-lieferung-abgeschickt">Werde ich informiert, wenn die Lieferung abgeschickt wurde?</h2>
	<p>Nein. UPS braucht in der Regel nur 1-2 Werktage. Möchten Sie trotzdem Ihr Paket verfolgen, rufen Sie uns gern unter <span class="tx_blue tx_12_15">+49.(0)30 28 47 00 0</span> an. </p>
	<br /><br />
	
	<h2 id="faq-bestaetigung">Ich habe per Fax oder Email bestellt. Bekomme ich eine Bestätigung meines Bestelleingangs?</h2>
	<p>Bei einer Mailbestellung antworten wir Ihnen innerhalb von 24h - falls Sie keine Mail von uns zurück bekommen, haben wir Ihre Bestellung wahrscheinlich nicht bekommen. Bitte rufen Sie uns in diesem Fall unter <br /><span class="tx_blue tx_12_15">+49.(0)30 28 47 00 0</span> an. Bestellen Sie per Fax, erhalten Sie keinerlei Bestätigung zurück.</p>
	<br /><br />
	
	<h2 id="faq-lieferanschrift">Meine Lieferanschrift hat sich geändert. Wie kann ich Ihnen dies mitteilen?</h2>
	<p>Bitte lassen Sie uns die neue Lieferanschrift via Fax, Email oder per Post zukommen, möglichst unabhängig von einem Bestellvorgang.</p>
